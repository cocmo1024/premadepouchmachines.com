function format(template, values) {
  return template.replace(/\{([a-zA-Z]+)\}/g, (_, key) => values[key] ?? "");
}

function titleFor(item, langCode) {
  if (langCode === "en") return item.title;
  return MACHINE_TITLES[item.slug]?.[langCode] || item.title;
}

function termFor(term, langCode) {
  if (langCode === "en") return term;
  return TERM_TRANSLATIONS[term]?.[langCode] || term;
}

function localizedList(list = [], langCode) {
  return list.map((item) => termFor(item, langCode));
}

function listPreview(items, langCode, count = 3) {
  const copy = LOCAL_MACHINE_COPY[langCode];
  const selected = localizedList(items, langCode).slice(0, count).filter(Boolean);
  if (!selected.length) return "";
  if (langCode === "en") {
    if (selected.length === 1) return selected[0];
    return `${selected.slice(0, -1).join(", ")} and ${selected.at(-1)}`;
  }
  if (selected.length === 1) return selected[0];
  return `${selected.slice(0, -1).join(", ")}${copy?.and || " and "}${selected.at(-1)}`;
}

function localizedSpecs(specs = [], langCode) {
  if (langCode === "en") return specs;
  const labels = SPEC_LABELS[langCode] || {};
  return specs.map(([label, value]) => [labels[label] || label, value]);
}

export function localizedMachine(item, langCode, localizedCategory) {
  if (langCode === "en") {
    return {
      title: item.title,
      h1: item.h1,
      summary: item.summary,
      productBehavior: item.productBehavior,
      lineFit: item.lineFit,
      applications: item.applications,
      packageStyles: item.packageStyles,
      materials: item.materials,
      workflow: item.workflow,
      features: item.features,
      options: item.options,
      specs: item.specs,
      relatedIntro: "",
      faqQuestions: null,
    };
  }

  const local = LOCAL_MACHINE_COPY[langCode];
  const title = titleFor(item, langCode);
  const applications = listPreview(item.applications, langCode, 3);
  return {
    title,
    h1: format(local.h1, { title, applications }),
    summary: format(local.summary, { category: localizedCategory, applications }),
    productBehavior: local.behavior,
    lineFit: local.lineFit,
    applications: localizedList(item.applications, langCode),
    packageStyles: localizedList(item.packageStyles, langCode),
    materials: localizedList(item.materials, langCode),
    workflow: localizedList(item.workflow, langCode),
    features: localizedList(item.features, langCode),
    options: localizedList(item.options, langCode),
    specs: localizedSpecs(item.specs, langCode),
    relatedIntro: local.relatedIntro,
    faqQuestions: {
      products: format(local.qProducts, { title }),
      options: format(local.qOptions, { title }),
      rfq: format(local.qRfq, { title }),
    },
  };
}

export function localizeMachineTerm(term, langCode) {
  return termFor(term, langCode);
}
