export function normalizePath(routePath) {
  if (!routePath || routePath === "/index.html") return "/";
  return routePath.startsWith("/") ? routePath : `/${routePath}`;
}

export function localizedPath(langCode, routePath) {
  const lang = LANGUAGES.find((item) => item.code === langCode);
  if (!lang) throw new Error(`Unknown language: ${langCode}`);
  const normalized = normalizePath(routePath);
  if (lang.code === "en") return normalized;
  if (normalized === "/") return `${lang.pathPrefix}/`;
  return `${lang.pathPrefix}${normalized}`;
}

export function absoluteUrl(langCode, routePath) {
  return `${BASE_URL}${localizedPath(langCode, routePath)}`;
}

export function copyFor(langCode) {
  return COPY[langCode] || COPY.en;
}

export function categoryFor(langCode, category) {
  const copy = copyFor(langCode);
  return copy.categories[category] || category;
}
